const getFormattedDate = (daysOffset) => {
  const d = new Date();
  d.setDate(d.getDate() + daysOffset);
  if (daysOffset === 0) return 'Today';
  if (daysOffset === 1) return 'Tomorrow';
  return d.toLocaleDateString('en-US', { weekday: 'short', day: 'numeric', month: 'short' });
};
